ESTADO VARCHAR(25) DEFAULT "-",
DETALLE VARCHAR(400) DEFAULT "-",
CONSTRAINT FK_ID_NODE_STATUS FOREIGN KEY (ID_NODE) REFERENCES NODES (ID)
